# string concatenation (aka how to put strings together)
# suppose we want to create a string that says "subscribe to _____ "
youtuber = "Pewdiepie" # some string varible

# a few ways to do this
print("subscribe to " + youtuber)           # what i usually use
print("subscribe to {}".format(youtuber))   # new 
print(f"subscribe to {youtuber}")           # new